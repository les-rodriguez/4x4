$('#clickMe1').click(function() {
    let isSwitched = $('#clickMe1').hasClass('switch');
if (isSwitched) {
    $('#clickMe1').removeClass('switch');
    $('#clickMe1').css({
      'background-color': 'black'
    });
  } else {
    $('#clickMe1').addClass('switch');
    $('#clickMe1').css({
      'background-color': 'grey'
    });
}
}); 
